/*
Vantagens do S.O.L.I.D:

1. Código mais fácil de manter:
   Cada classe tem uma única responsabilidade, o que reduz efeitos colaterais e facilita correções.

2. Maior reutilização de código:
   Classes e módulos ficam mais independentes, permitindo reaproveitar em outros contextos.

3. Facilita testes unitários:
   Como as dependências são injetadas e o acoplamento é baixo, fica simples usar mocks e stubs.

4. Escalabilidade:
   O sistema cresce sem virar um caos, porque cada parte tem fronteiras bem definidas.

5. Menos acoplamento:
   Limita dependências rígidas entre classes e torna o código mais flexível para mudanças.


Desvantagens do S.O.L.I.D:

1. Mais código para escrever:
   Ao aplicar interfaces, classes, injeção de dependência etc., o projeto tende a ter mais arquivos e estrutura.

2. Curva de aprendizado:
   Para iniciantes, certos princípios — principalmente OCP, LSP e DIP — podem ser difíceis de entender.

3. Complexidade desnecessária em projetos pequenos:
   Em sistemas simples, usar todos os princípios pode ser "overkill", deixando o código mais burocrático.

4. Exagero nas abstrações:
   Criar interfaces e camadas demais pode tornar o código mais complicado do que deveria ser.
*/
